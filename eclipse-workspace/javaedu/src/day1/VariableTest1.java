package day1;

public class VariableTest1 {
	public static void main(String[] args) {
		System.out.println(1+2+3+4+5 + 10); // 25
		System.out.println(1+2+3+4+5 - 10);  // 5
		System.out.println((1+2+3+4+5) * 10);  // 150
		System.out.println((1+2+3+4+5) / 10); // 몫 - 1
		System.out.println((1+2+3+4+5) % 10); // 나머지 - 5
		int num = 1+2+3+4+5;
		System.out.println(num + 10); // 25
		System.out.println(num - 10);  // 5
		System.out.println(num * 10);  // 150
		System.out.println(num / 10); // 몫 - 1
		System.out.println(num % 10); // 나머지 - 5
	}
}
