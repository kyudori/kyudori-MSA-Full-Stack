package day2;

public class LiteralTest2_1 {
	public static void main(String[] args) {
		System.out.print(10);
		System.out.print(-10);
		System.out.print(10.0);
		System.out.print(10.0f);
		System.out.print(10L);
		System.out.print(true);
		System.out.print(false);
		System.out.print('A');
		System.out.print("A");
		System.out.print('가');
		System.out.print("가");
		System.out.print("ABC");
		System.out.print("가나다");
	}
}
